_LOOKUP: dict[str, str] = {k.lower(): k for k in ABILITIES}


def get_abilities(species: str) -> tuple[str, ...]:
    """Get abilities for a species (case-insensitive).

    Returns a tuple of ability names, or an empty tuple if species is unknown.
    """
    key = _LOOKUP.get(species.lower())
    if key is None:
        return ()
    return ABILITIES[key]
