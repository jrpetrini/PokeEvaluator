# Build case-insensitive lookup index
_LOOKUP: dict[str, str] = {name.lower(): name for name in POKEDEX}


def get_base_stats(species: str) -> BaseStats:
    """Look up base stats by species name (case-insensitive)."""
    key = species.lower()
    if key not in _LOOKUP:
        raise ValueError(
            f"Pokémon desconhecido: {species!r}. "
            f"Use 'pokeval pokedex' para listar os disponíveis."
        )
    return POKEDEX[_LOOKUP[key]]
