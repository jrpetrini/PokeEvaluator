const _LOOKUP: Record<string, string> = {};
for (const name of Object.keys(POKEDEX)) {
  _LOOKUP[name.toLowerCase()] = name;
}

export const POKEMON_NAMES = Object.keys(POKEDEX);

export function getBaseStats(species: string): BaseStats {
  const key = _LOOKUP[species.toLowerCase()];
  if (!key) throw new Error(`Unknown Pokémon: ${species}`);
  return POKEDEX[key];
}

export function findSpeciesName(species: string): string | null {
  return _LOOKUP[species.toLowerCase()] ?? null;
}
